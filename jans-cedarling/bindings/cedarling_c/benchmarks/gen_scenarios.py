#!/usr/bin/env python3
"""Generate scenarios_generated.h from the shared manifest.
C has no built-in JSON parser, so the bench consumes pre-formatted string literals."""

from __future__ import annotations

import json
import sys
from pathlib import Path


def c_escape(s: str) -> str:
    # JSON escape is a subset of C escape for our characters (\" and \\).
    return json.dumps(s)[1:-1]


def build_config_template(scenario: dict) -> str:
    # %s survives json.dumps unchanged; the C bench fills the abs path at runtime.
    overrides = dict(scenario.get("config_overrides") or {})
    overrides["CEDARLING_POLICY_STORE_LOCAL_FN"] = "%s"
    return json.dumps(overrides, separators=(",", ":"))


def build_request_json(scenario: dict) -> str:
    # Context is JSON-as-string in the manifest; parse back to an object so the embed is well-formed.
    ctx_str = scenario.get("context") or "{}"
    ctx = json.loads(ctx_str) if ctx_str else {}

    kind = scenario["kind"]
    if kind == "unsigned":
        req = {
            "action": scenario["action"],
            "resource": scenario["resource"],
            "context": ctx,
        }
        if scenario.get("principal") is not None:
            req["principal"] = scenario["principal"]
        return json.dumps(req, separators=(",", ":"))
    if kind == "multi_issuer":
        req = {
            "tokens": scenario.get("tokens", []),
            "resource": scenario["resource"],
            "action": scenario["action"],
            "context": ctx,
        }
        return json.dumps(req, separators=(",", ":"))
    raise ValueError(f"unknown scenario kind: {kind}")


HEADER_PREAMBLE = """\
/* This file is GENERATED by gen_scenarios.py from
   bindings/benchmarks/fixtures/scenarios.json. Do not edit by hand. */
#ifndef CEDARLING_BENCH_SCENARIOS_GENERATED_H
#define CEDARLING_BENCH_SCENARIOS_GENERATED_H

#include <stddef.h>

typedef struct {
    const char *id;
    const char *kind;              /* "unsigned" | "multi_issuer" */
    const char *policy_store_fn;   /* repo-root-relative */
    const char *config_template;   /* sprintf format with one %s for the abs policy-store path */
    const char *request_json;      /* fully baked request payload */
    int mock_op_required;
} bench_scenario_t;

"""

HEADER_TAIL = """
#endif /* CEDARLING_BENCH_SCENARIOS_GENERATED_H */
"""


def main() -> int:
    # Script lives at bindings/cedarling_c/benchmarks/, repo root is 3 levels up.
    this_file = Path(__file__).resolve()
    repo_root = this_file.parents[3]
    manifest_path = repo_root / "bindings" / "benchmarks" / "fixtures" / "scenarios.json"
    with manifest_path.open() as f:
        manifest = json.load(f)

    out_lines = [HEADER_PREAMBLE]
    policy = manifest.get("iteration_policy", {})
    out_lines.append(
        f"#define BENCH_WARMUP_ITERS  {int(policy.get('warmup_iters', 100))}\n"
    )
    out_lines.append(
        f"#define BENCH_MEASURE_ITERS {int(policy.get('measure_iters', 1000))}\n\n"
    )
    out_lines.append("static const bench_scenario_t SCENARIOS[] = {\n")

    for s in manifest.get("scenarios", []):
        mock = 1 if s.get("mock_op_required") else 0
        if mock:
            # mock-op scenarios still get a row so the bench can emit a skipped JSONL line.
            out_lines.append(
                "    {\n"
                f'        .id = "{c_escape(s["id"])}",\n'
                f'        .kind = "{c_escape(s["kind"])}",\n'
                f'        .policy_store_fn = "{c_escape(s["policy_store_fn"])}",\n'
                f'        .config_template = "",\n'
                f'        .request_json = "",\n'
                "        .mock_op_required = 1,\n"
                "    },\n"
            )
            continue
        cfg_tpl = build_config_template(s)
        req = build_request_json(s)
        out_lines.append(
            "    {\n"
            f'        .id = "{c_escape(s["id"])}",\n'
            f'        .kind = "{c_escape(s["kind"])}",\n'
            f'        .policy_store_fn = "{c_escape(s["policy_store_fn"])}",\n'
            f'        .config_template = "{c_escape(cfg_tpl)}",\n'
            f'        .request_json = "{c_escape(req)}",\n'
            "        .mock_op_required = 0,\n"
            "    },\n"
        )
    out_lines.append("};\n")
    out_lines.append(
        "#define NUM_SCENARIOS (sizeof(SCENARIOS) / sizeof(SCENARIOS[0]))\n"
    )
    out_lines.append(HEADER_TAIL)

    out_path = this_file.parent / "scenarios_generated.h"
    out_path.write_text("".join(out_lines))
    print(f"wrote {out_path}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
