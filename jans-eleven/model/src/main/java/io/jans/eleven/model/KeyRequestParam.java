package io.jans.eleven.model;

/**
 * @author Javier Rojas Blum
 * @version April 18, 2016
 */
public class KeyRequestParam {

    private String alg;
    private String kid;
    private String use;
    private String kty;
    private String n;
    private String e;
    private String crv;
    private String x;
    private String y;

    public KeyRequestParam() {
    }

    public KeyRequestParam(String kty, String use, String alg, String kid) {
        this(kty, use, alg, null, kid);
    }

    public KeyRequestParam(String kty, String use, String alg, String crv, String kid) {
        this.kty = kty;
        this.use = use;
        this.alg = alg;
        this.crv = crv;
        this.kid = kid;
    }

    public String getAlg() {
        return alg;
    }

    public void setAlg(String alg) {
        this.alg = alg;
    }

    public String getKid() {
        return kid;
    }

    public void setKid(String kid) {
        this.kid = kid;
    }

    public String getUse() {
        return use;
    }

    public void setUse(String use) {
        this.use = use;
    }

    public String getKty() {
        return kty;
    }

    public void setKty(String kty) {
        this.kty = kty;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getE() {
        return e;
    }

    public void setE(String e) {
        this.e = e;
    }

    public String getCrv() {
        return crv;
    }

    public void setCrv(String crv) {
        this.crv = crv;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
